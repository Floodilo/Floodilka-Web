package integration

import (
	"fmt"
	"net/http"
	"testing"
)

func joinGuild(t testing.TB, client *testClient, token, inviteCode string) {
	t.Helper()
	resp, err := client.postJSONWithAuth(fmt.Sprintf("/invites/%s", inviteCode), nil, token)
	if err != nil {
		t.Fatalf("failed to accept invite: %v", err)
	}
	assertStatus(t, resp, http.StatusOK)
	resp.Body.Close()
}
